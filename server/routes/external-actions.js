import { Router } from "express";
import * as externalActions from "../external-actions.js";
import * as artifacts from "../artifacts.js";
import { getPorkbunCredentials } from "../config.js";
import { purchaseDomain } from "../providers/porkbun.js";
import { emitSSE } from "./pipeline.js";

const router = Router();

// ---------------------------------------------------------------------------
// POST /api/external-actions/request
// Create a new pending action. Emits SSE external-action-requested.
// Body: { projectId, type, params }
// ---------------------------------------------------------------------------

router.post("/request", (req, res) => {
  const { projectId, type, params } = req.body;

  if (!projectId || !type) {
    return res.status(400).json({
      error: "Missing required fields",
      reason: "projectId and type are required",
    });
  }

  // ── Simulated data guard (D052) ──────────────────────────────────────
  if (type === "domain-purchase") {
    const domain = params?.domain;
    if (domain) {
      const projectArtifacts = artifacts.getByProject(projectId);
      const domainArtifact = projectArtifacts.find(
        (a) => a.type === "domain_signals",
      );
      if (
        domainArtifact?.metadata?.engine === "n8n-plan-domain-v1-simulated"
      ) {
        console.log(
          `[external-actions] request rejected: simulated data guard projectId=${projectId} domain=${domain}`,
        );
        return res.status(400).json({
          error: "Simulated data rejected",
          reason:
            "Domain signals were generated by a simulated engine (n8n-plan-domain-v1-simulated). Real domain data is required before purchasing.",
        });
      }
    }
  }

  try {
    const action = externalActions.requestAction({ projectId, type, params });
    emitSSE(projectId, "external-action-requested", {
      actionId: action.id,
      type: action.type,
      params: action.params,
      status: action.status,
      requestedAt: action.requestedAt,
    }, req.lumonUser?.login ?? null);
    console.log(
      `[external-actions] POST /request projectId=${projectId} actionId=${action.id}`,
    );
    res.status(201).json(action);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
});

